# Declaraciones If: una declaracion que usamos para ejecutar codigo solo cuando alguna condicion sea satisfecha

# Similar a la manera en que tomamos decisiones.
# Ej: Si hoy esta lloviendo -> llevo sombrilla
#     de lo contrario -> no llevo sombrilla

favoriteNumber = 10

if favoriteNumber > 30:
    print("Este numero es mayor a treinta")
elif favoriteNumber > 20:
    print("Este numero es mayor a 20")
else:
    print("Este numero es menor o igual a 20")
