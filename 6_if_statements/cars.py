# Declaraciones If: una declaracion que usamos para ejecutar codigo solo cuando alguna condicion sea satisfecha

# Similar a la manera en que tomamos decisiones.
# Ej: Si hoy esta lloviendo -> llevo sombrilla
#     de lo contrario -> no llevo sombrilla

# Operadores para hacer Comparaciones:

# Igualdad: age == 18
# Desigualdad: age != 18
# Menor: age < 18
# Igual o menor: age <= 18
# Mayor: age > 18
# Igual o mayor: age >= 18

car = 'bmw'

if car == 'bmw' or car == 'audi':
    print('Esta marca es bmw o audi')
else:
    print('Esta marca de carros no es igual a bmw o audi')
